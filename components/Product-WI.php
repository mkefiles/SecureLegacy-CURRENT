    <h2>Worksite Insurance</h2>
    <h3>What Are Supplemental Benefits?</h3>
    <p>
        Supplemental benefits allow you to meet your medical and financial needs. They enhance the health insurance you may already have. Also known as voluntary benefits,  enrolling in a plan helps to fill coverage gaps and/or to help with medical bills.
    </p>
    <h3>Do I Need Supplemental Benefits?</h3>
    <p>
        Imagine something happens to you or your family- are you prepared to pay the deductibles, copays, coinsurance, and/or out-of-pocket maximum? Even if you have that covered- could you then afford to miss work? Then you need to consider Supplemental Benefits.
    </p>
    <h3>How Do These Benefits Work?</h3>
    <p>
        Many people are unfamiliar with Supplemental Benefits and often confuse them with health insurance. These benefits are very different from health insurance because:
        <br>
        <span class="bullet-text">Payment is sent to the Insured (you) directly</span>
        <br>
        <span class="bullet-text">Benefits may be used for ANY purpose (medical bills, household expenses, childcare, etc.)</span>
        <br>
        <span class="bullet-text">No deductibles or copays required to receive payment</span>
        <br>
        <span class="bullet-text">Plans start at less than $1/day</span>
    </p>
    <h3>What Supplemental Benefits Do You Offer?</h3>
    <p>
        We offer a wide-range of affordable options and can provide them directly to you or through your employer. Depending on the plan, you may have the option to cover your spouse and/or dependent children.
    </p>
    <h4>Accident</h4>
    <p>
        These plans pay lump-sum benefits for covered on/off the job accidents and offer a range of benefits for simple or complex accidents. This is coverage for IF something happens, and may be fitting if you:
        <br>
        <span class="bullet-text">Have children</span>
        <br>
        <span class="bullet-text">Are otherwise healthy</span>
        <br>
        <span class="bullet-text">Lead an active lifestyle</span>
        <br>
        <span class="bullet-text">Work in a blue-collar industry</span>
    </p>
    <h4>Hospital Indemnity</h4>
    <p>
        These plans pay upon incurred costs of a hospital stay and/or other medical procedures due to a covered accident or illness. This is coverage for WHEN something happens, and may be fitting if you:
        <br>
        <span class="bullet-text">Have Disability Insurance that includes an elimination period</span>
        <br>
        <span class="bullet-text">Are enrolled in a high-deductible health plan (HDHP)</span>
        <br>
        <span class="bullet-text">Have a family history of sickness/illness</span>
        <br>
        <span class="bullet-text">Are in a risky profession</span>
        <br>
        <span class="bullet-text">Plan on becoming pregnant</span>
    </p>
    <h4>Critical Illness</h4>
    <p>
        These plans pay when diagnosed and/or treated for a covered critical illness (heart attack, stroke, cancer, etc.). This is coverage for IF a covered critical illness is diagnosed, and may be fitting if you:
        <br>
        <span class="bullet-text">Have a family history of a covered critical illness (cancer, heart attacks, strokes, etc.)</span>
        <br>
        <span class="bullet-text">Are at an age where critical illness is more likely</span>
        <br>
        <span class="bullet-text">Are constantly or increasingly stressed</span>
    </p>
